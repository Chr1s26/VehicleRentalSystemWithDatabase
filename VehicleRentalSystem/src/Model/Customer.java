package Model;

public class Customer {

	private String name;
	private int id;
	
	public Customer(String name) {
		this.name = name;
	}
	
	public Customer(int id,String name) {
		this.id = id;
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	@Override
	public String toString() {
		return "Customer [name=" + name + ", id=" + id + "]";
	}

}
